            <h1>ABOUT US</h1>
            <p>ADDED COLOR is a Brazilian/American rock band formed in Brooklyn, NY, comprised of Brazilian brothers Dan and Kiko Freiberg, Tim Haggerty, and Danny Dahan. Since meeting in 2014, the group has played over 200 shows in over 20 states in the USA, and toured internationally in Canada, Morocco, and Brazil. They have played major festivals in Brazil such as Porão do Rock, which has featured Muse, Eagles of Death Metal, amongst others. While they have only been called Added Color since May, 2017, the band’s previous project under the same formation received critical acclaim from significant press outlets such as PureVolume, Red Bull Music, The Huffington Post, AOL Noisecreep, and The Village Voice, with The Monsters of Rock Festival officially nominating them as one of the top five upcoming Brazilian rock bands.</p>
            <p>Added Color made their debut on May 9, 2017 releasing their 5-song EP entitled “Psycho”. The EP received the backing of an international tour, beginning in Morocco and was followed by a 6-week US Tour. Pancakes and Whiskey debuted the title single off the “Psycho” EP by stating “If you’re ready to hear the best rock track of the year, thus far, then you’ve come to the right place.” </p>
            <p>Added Color's second EP, "Mr. Industry" was released on April 20, 2018. The EP was recorded in Brooklyn at Gojira's Silver Cord Studios, by Johann Meyer (Gojira/Roadrunner Records) and Jamie Uertz (Blind Melon), and produced by Added Color. Mr. Industry was backed by a second Morocco tour with 14 dates, and a one month long US summer tour.</p>
            <p>Since their second EP, Added Color has continued to tour consistently and has gone back into Silver Cord Studios to record their upcoming EP "if you had it all". This time the full production was done at Silver Cord, with Jamie Uertz on tracking and Johann Meyer on mixing.</p>
            <p>Whether they’re playing big stages in Brazilian festivals or more intimate venues, Added Color always brings a high energy performance to the audience. Their message: be yourself no matter what, respect others for who they are, and don’t play into people’s expectations of who you should be.</p>
